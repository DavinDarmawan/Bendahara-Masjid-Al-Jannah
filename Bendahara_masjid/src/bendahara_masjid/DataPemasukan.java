    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        // 1. Dapatkan baris yang dipilih oleh pengguna dari tabel
        int barisTerpilih = tabelPemasukan.getSelectedRow();

        // 2. Pastikan ada baris yang dipilih
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return; // Hentikan eksekusi jika tidak ada baris yang dipilih
        }

        // 3. Tampilkan dialog konfirmasi sebelum menghapus
        int pilihan = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION);

        // 4. Lanjutkan hanya jika pengguna memilih "Yes"
        if (pilihan == JOptionPane.YES_OPTION) {
            try {
                // Ambil ID dari kolom pertama (indeks 0) pada baris yang dipilih
                int idUntukDihapus = (int) tabelPemasukan.getValueAt(barisTerpilih, 0);

                // Buat perintah SQL DELETE
                String sql = "DELETE FROM pemasukan WHERE id_pemasukan = ?";
                java.sql.Connection conn = koneksidb.getKoneksi(); // Pastikan nama kelas koneksi benar
                java.sql.PreparedStatement ps = conn.prepareStatement(sql);

                // Set ID untuk klausa WHERE
                ps.setInt(1, idUntukDihapus);

                // Eksekusi perintah delete
                ps.executeUpdate();

                // Tampilkan pesan sukses
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");

                // Muat ulang data di tabel untuk menampilkan perubahan
                tampilkanDataPemasukan();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_hapusActionPerformed


