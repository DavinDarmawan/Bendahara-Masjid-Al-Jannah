    private void tampilkanDataPemasukan() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Tanggal");
        model.addColumn("Sumber");
        model.addColumn("Jumlah (Rp)");
        model.addColumn("Keterangan");

        try {
            String sql = "SELECT * FROM pemasukan ORDER BY tanggal DESC";
            Connection conn = koneksidb.getKoneksi(); // Pastikan nama kelas koneksi Anda benar
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getInt("id_pemasukan"),
                    res.getString("tanggal"),
                    res.getString("sumber"),
                    res.getBigDecimal("jumlah"),
                    res.getString("keterangan")
                });
            }
            // Mengatur model yang sudah diisi data ke JTable Anda
            tabelPemasukan.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data pemasukan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

