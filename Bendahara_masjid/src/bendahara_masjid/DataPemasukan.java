    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        // 1. Dapatkan baris yang dipilih dari tabel
        int barisTerpilih = tabelPemasukan.getSelectedRow();

        // 2. Pastikan ada baris yang dipilih
        if (barisTerpilih == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin di-edit terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Ambil ID dari kolom pertama (indeks 0)
        int idUntukDiedit = (int) tabelPemasukan.getValueAt(barisTerpilih, 0);

        // 4. Pindah ke panel edit sambil mengirimkan ID
        mainPanel.removeAll();
        // Kita akan buat panel PanelEditPemasukan di langkah berikutnya
        mainPanel.add(new EditPemasukan(idUntukDiedit, mainPanel));
        mainPanel.revalidate();
        mainPanel.repaint();
    }//GEN-LAST:event_editActionPerformed

