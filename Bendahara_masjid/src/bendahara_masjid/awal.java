    private void tampilkanData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tanggal");
        model.addColumn("Jenis Transaksi");
        model.addColumn("Deskripsi");
        model.addColumn("Masuk (Rp)");
        model.addColumn("Keluar (Rp)");

        try {
            // Gunakan query UNION ALL yang sudah kita rancang
            String sql = "SELECT tanggal, 'Pemasukan' AS jenis, sumber AS deskripsi, jumlah AS masuk, 0 AS keluar FROM pemasukan " +
                         "UNION ALL " +
                         "SELECT tanggal, 'Pengeluaran' AS jenis, kegiatan AS deskripsi, 0 AS masuk, jumlah AS keluar FROM pengeluaran " +
                         "ORDER BY tanggal DESC";

            Connection conn = koneksidb.getKoneksi(); // Pastikan nama kelas koneksi benar
            Statement stm = conn.createStatement();
            ResultSet res = stm.executeQuery(sql);

            while (res.next()) {
                model.addRow(new Object[]{
                    res.getString("tanggal"),
                    res.getString("jenis"),
                    res.getString("deskripsi"),
                    res.getBigDecimal("masuk"),
                    res.getBigDecimal("keluar")
                });
            }
            // Mengatur model ke JTable Anda
            tabel.setModel(model); 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat log data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

