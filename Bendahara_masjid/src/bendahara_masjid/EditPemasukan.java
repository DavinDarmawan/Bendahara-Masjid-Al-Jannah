    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
       try {
            String sql = "UPDATE pemasukan SET tanggal = ?, sumber = ?, jumlah = ?, keterangan = ? WHERE id_pemasukan = ?";
            Connection conn = koneksidb.getKoneksi();
            PreparedStatement ps = conn.prepareStatement(sql);

            // Ambil nilai baru dari form
            java.util.Date tgl = tanggal.getDate();
            java.sql.Date sqlTgl = new java.sql.Date(tgl.getTime());
            BigDecimal jml = new BigDecimal(nominal.getText());

            // Set parameter untuk query UPDATE
            ps.setDate(1, sqlTgl);
            ps.setString(2, Sumber.getSelectedItem().toString());
            ps.setBigDecimal(3, jml);
            ps.setString(4, keterangan.getText());
            ps.setInt(5, idToEdit); // ID untuk klausa WHERE

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data pemasukan berhasil diperbarui!");

            // Kembali ke halaman daftar data pemasukan
            mainPanel.removeAll();
            mainPanel.add(new DataPemasukan(mainPanel)); 
            mainPanel.revalidate();
            mainPanel.repaint();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage());
        }
    }//GEN-LAST:event_simpanActionPerformed

