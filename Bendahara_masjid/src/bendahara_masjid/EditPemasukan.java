     private void muatData() {
        try {
            String sql = "SELECT * FROM pemasukan WHERE id_pemasukan = ?";
            Connection conn = koneksidb.getKoneksi();
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idToEdit);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                // Set nilai dari database ke komponen form
                tanggal.setDate(rs.getDate("tanggal"));
                Sumber.setSelectedItem(rs.getString("sumber"));
                nominal.setText(String.valueOf(rs.getBigDecimal("jumlah")));
                keterangan.setText(rs.getString("keterangan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data untuk diedit: " + e.getMessage());
        }
    }

